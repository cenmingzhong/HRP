package com.gzhh.hrp.common;

public class DataGridColTitle {

    private String titleKey;
    private String titleDataCode;
    private String titleDataText;
    private int titleDataOrder;
    private String titleParentCode;
    
    public String getTitleKey() {
        return titleKey;
    }
    public void setTitleKey(String titleKey) {
        this.titleKey = titleKey;
    }
    public String getTitleDataCode() {
        return titleDataCode;
    }
    public void setTitleDataCode(String titleDataCode) {
        this.titleDataCode = titleDataCode;
    }
    public String getTitleDataText() {
        return titleDataText;
    }
    public void setTitleDataText(String titleDataText) {
        this.titleDataText = titleDataText;
    }
    public int getTitleDataOrder() {
        return titleDataOrder;
    }
    public void setTitleDataOrder(int titleDataOrder) {
        this.titleDataOrder = titleDataOrder;
    }
    public String getTitleParentCode() {
        return titleParentCode;
    }
    public void setTitleParentCode(String titleParentCode) {
        this.titleParentCode = titleParentCode;
    }
    
    
}
